main_dict = {
    'One': 'Un(Une)',
    'Two': 'Deux',
    'Three': 'Trois',
    'Four': 'quatre',  
    'Five': 'cinq',
    }

print('---------------------------------------------------------------------')
print('Англо-французский словарь. V.1.0.0')
print('---------------------------------------------------------------------')
for key, value in main_dict.items():
    print(key, ' - ', value, sep='')
print('---------------------------------------------------------------------')

a=5
while a>1:

    print('Возможные действия:\n1) добавить новые слова; 2) удалить слова;' 
        ' 3) заменить слова; 4) найти слова; 5) выйти')
    print('---------------------------------------------------------------------')

    main_choice = int(input('Введите номер нужного действия: '))
    print('---------------------------------------------------------------------')

    if main_choice == 1:
        en_1 = input('Введите английское слово: ')
        fr_1 = input('Введите перевод на французский: ')
        print('---------------------------------------'
        '------------------------------')
        print(en_1, ' - ', fr_1)
        choice_1=input('Вы уверенны? (y/n): ')
        print('---------------------------------------'
        '------------------------------')
        if choice_1 == 'y':
            main_dict[en_1]=fr_1
            for key, value in main_dict.items():
                print(key, ' - ', value, sep='')
        else: 
            exit

    elif main_choice == 2:
        remove = input('Введите пару, которую необходимо удалить (англ.): ')
        print('---------------------------------------'
        '------------------------------')
        print(remove, ' - ', main_dict[remove])
        choice_2=input('Вы уверенны? (y/n): ')
        print('---------------------------------------'
        '------------------------------')
        if choice_2 == 'y':
            del main_dict[remove]
            for key, value in main_dict.items():
                print(key, ' - ', value, sep='')
        else: 
            exit

    elif main_choice == 3:
        replace = input('Введите слово на английском: ')
