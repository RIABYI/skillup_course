        print('---------------------------------------'
        '------------------------------')
        if choice_3 == 'y':
            choice_4 = int(input('Введите что заменить: 1) английское слово;'
                            '2) перевод на французский: '))
            if choice_4 == 1:
                en_2 = input(f'Введите новое значение вместо {replace}: ')
                print('---------------------------------------'
                '------------------------------')
                print(en_2, ' - ', main_dict[replace])
                choice_5=input('Вы уверенны? (y/n): ')
                print('---------------------------------------'
                '------------------------------')
                if choice_5 == 'y':
                    main_dict[en_2]=main_dict.pop(replace)
                    for key, value in main_dict.items():
                        print(key, ' - ', value, sep='')
            if choice_4 == 2:
                fr_2 = input(f'Введите новое значение вместо {main_dict[replace]}: ')
